package JAVA;

public class TiposDeDatos {

    public class Primitivos {

        // desde -128 hasta 127
        byte myByte = -128;

        // desde -32,768 hasta 32,767
        short myShort = 32767;

        // desde -2,147,483,648 hasta 2,147,483,647
        int myInt = -2147483648;

        // muy grande...
        long myLong = 9333333L;
    }

    public class Decimales {

        float myFloat = 2.333333f;
        double myDouble = 3.333333333;
    }

    public class Otros {

        char myChar = '1';
        boolean myBoolean = true;
    }

    public class Objeto {

        String myString = "Hola Mundo";
    }
}
