
package cl.daniel.poo;

/**
 * The Auto class represents a car.
 */
public class Auto {

    /**
     * The license plate of the car.
     */
    String patente;

    /**
     * The color of the car.
     */
    String color;

    /**
     * The number of kilometers the car has traveled.
     */
    int kms;

    /**
     * Indicates whether the car has air conditioning or not.
     */
    boolean conAire;

}
